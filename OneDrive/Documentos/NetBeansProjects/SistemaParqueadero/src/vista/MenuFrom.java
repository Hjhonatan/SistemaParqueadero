    private void jButton_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_buscarActionPerformed

        String PlacaPropietarioBusqueda = jTextField_busqueda_criterio.getText().trim();
        Date FechaBusqueda = jDateChooser_fecha_bucar.getDate();
        String FechaCadena = "";

        if (FechaBusqueda != null) {

            SimpleDateFormat smp = new SimpleDateFormat("yyyy/MM/dd");
            FechaCadena = smp.format(FechaBusqueda);
        }

        if (PlacaPropietarioBusqueda.isEmpty() && FechaCadena.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el propietario o la placa del vehiculo");
            this.CargarTablaVehiculos();

        } else {

            VehiculoControlador VehiculoCon = new VehiculoControlador();

            try {
                VehiculoCon.buscarVehiculosPlacasFecha(PlacaPropietarioBusqueda, FechaCadena);
            } catch (SQLException ex) {
                Logger.getLogger(MenuFrom.class.getName()).log(Level.SEVERE, null, ex);
            }
            ArrayList<Vehiculo> ListaVehiculo = VehiculoCon.listaVehiculo;

            DefaultTableModel model = new DefaultTableModel();

            this.jTable_vehiculos = new JTable(model);
            this.jScrollPane1.setViewportView(this.jTable_vehiculos);

            model.addColumn("N°1");
            model.addColumn("Placa");
            model.addColumn("Propietario");
            model.addColumn("Tipo de Vehiculo");
            model.addColumn("Valor Pagado");
            model.addColumn("Estado");

            Object fila[] = new Object[6];

            for (Vehiculo vehiculo : ListaVehiculo) {
                fila[0] = vehiculo.getIdVehiculo();
                fila[1] = vehiculo.getPlaca();
                fila[2] = vehiculo.getPropietario();
                fila[3] = vehiculo.getTipoVehiculo();
                fila[4] = vehiculo.getValorPagado();
                fila[5] = vehiculo.getEstado();
                model.addRow(fila);

            }
            
            jTextField_busqueda_criterio.setText("");
        }

    }//GEN-LAST:event_jButton_buscarActionPerformed

